    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        df = ( DefaultTableModel)jTable1.getModel();
        int selected = jTable1.getSelectedRow();
        int id = Integer.parseInt(df.getValueAt(selected, 0).toString());
        
        String member = txtmem.getSelectedItem().toString();
        SimpleDateFormat df = new  SimpleDateFormat("yyyy-MM-dd");
        String date  = df.format(txtdate.getDate());
        String amount = txtamo.getText();
        String plan = txtplan.getSelectedItem().toString();
       
        
        try {
            pst = con.prepareStatement("update payment set name = ?, date = ?, amount = ?, plan = ? where id = ?");
            pst.setString(1, member);
            pst.setString(2, date);
            pst.setString(3, amount);
            pst.setString(4, plan);
            pst.setInt(5, id);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "PAYMENT UPDATED");
            
            txtamo.setText("");
            txtamo.requestFocus();
            
            jButton1.setEnabled(true);
            table_load();
            
        } catch (SQLException ex) {
            Logger.getLogger(trainer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

