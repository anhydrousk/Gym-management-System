    public void table_load()
    {
        try {
            pst = con.prepareStatement("select * from payment");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData rd = rs.getMetaData();
            int c;
            c = rd.getColumnCount();
            df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for(int i=1; i<=c; i++)
                {
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("name"));
                    v2.add(rs.getString("date"));
                    v2.add(rs.getString("amount"));
                    v2.add(rs.getString("plan"));
                    
                    
                    
                    
                    
                    
                    
                }
                
                
                
                df.addRow(v2);
                
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(trainer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
    }
    
    public void load_member()
    {
         try {
            pst = con.prepareStatement("select Distinct firstname from member");
            
            ResultSet rs;
            rs = pst.executeQuery();
            txtmem.removeAllItems();
            
            
            
            while(rs.next())
            {
                txtmem.addItem(rs.getString("firstname"));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(member.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

